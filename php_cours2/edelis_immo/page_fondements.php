				<h2>nos engagements</h2>

				<p>Les exigences de nos clients sont en constante évolution. Face à ces nouveaux enjeux, nous nous engageons à produire :</p>

			</div>

			<ul>

				<li>

					<h3>Des logements de qualité.</h3>

					<p>La satisfaction de nos  clients est au cœur de nos préoccupations. Les attentes des municipalités en matière d’urbanisme et de développement durable, l’est autant. Dès la conception du projet, nous portons un soin tout particulier à ce que les moyens employés répondent à des critères de qualité très ambitieux : partenariat avec les meilleurs architectes et bureau d’études, sélection très soigneuse de nos prestataires, exigeants cahiers des charges basés sur la réglementation thermique et acoustique</p>

				</li>

				<li>

					<h3>Un immobilier durable.</h3>

					<p>Conscients des enjeux majeurs en termes environnemental, nous optons pour une approche écoresponsable et proactive qui vise à maitriser la consommation en énergie et en eau de nos bâtiments, à réduire les émissions de CO2 et à optimiser la gestion des déchets. Pour aller plus loin, nous sommes en mesure de proposer à nos clients, au-delà du simple respect de la Réglementation Thermique 2012, des logements certifiés Habitat & Environnement et NF Logement HQE (Haute Qualité Environnementale).</p>

				</li>

				<li>

					<h3>Des logements pour tous.</h3>

					<p>Nous revendiquons une qualité accessible à tous : nos premiers prix -logements sociaux ou en accession à la propriété- bénéficient de la même exigence de qualité que nos autres produits. Tous nos logements sont personnalisables avant livraison, tant au niveau des matériaux et des finitions que d’éventuelles modifications requérant des travaux plus importants.</p>

				</li>

			</ul>

