				<h2>nos missions</h2>

				<p>Par notre politique d’offres résidentielles ouvertes à tous, nous participons à la création de villes à échelle humaine, durables et où il fait bon  vivre. Dans cette perspective, nous nous sommes fixés  3 missions prioritaires :</p>

			</div>

			<ul>

				<li>

					<h3>CONSTRUIRE DES PATRIMOINES DE VALEUR</h3>

					<p>Que vous envisagiez un projet immobilier dans le cadre d’une résidence ou d’une démarche d’investissement, nous vous aidons à vous constituer un patrimoine de valeur en vous offrant des biens de qualité, économes en énergie et bénéficiant d’emplacements attractifs. </p>

				</li>

				<li>

					<h3>Créer de nouveaux cadres de vie</h3>

					<p>Parce que nous pensons que le cadre de vie influe directement sur la qualité de vie et le bien-être, nous nous attachons à créer des résidences confortables, performantes et sécurisées. Nos réalisations bénéficient de prestations de qualité, disposent d’espaces extérieurs paysagés et se situent à proximité de toutes commodités : transports en commun, écoles, commerces… <br/>

					Elles contribuent à un quotidien apaisé.</p>

				</li>

				<li>

					<h3>Créer des logements pour tous</h3>

					<p>Nous croyons à la mixité sociale et générationnelle, cette mixité qui permet le développement de villes vivantes et diversifiées. Dans cette perspective, nous avons bâti une réelle politique de logements pour tous : large typologie du studio au 5 pièces, prix maitrisés, dispositions spécifiques telles que le prêt à l’accession sociale, la TVA réduite ou les résidences étudiantes et de services…</p>

				</li>

